// ListenExportXML 监听并处理申报国家的 Export XML 文件
// declareCountry: 申报国家代码 (NL 或 BE)
func ListenExportXML(declareCountry string) {
	if "NL" != declareCountry && "BE" != declareCountry {
		log.Panicf("%s 不是有效的申报国家(NL | BE)", declareCountry)
	}

	// 获取对应国家的监控目录
	watchDir := viper.GetString(fmt.Sprintf("watcher.%s.watch-dir", strings.ToLower(declareCountry)))
	if watchDir == "" {
		log.Panicf("申报国家 %s 的监控目录未配置", declareCountry)
	}

	// 确保监控目录存在
	if !util.IsDir(watchDir) {
		log.Panicf("监控目录: %s 不存在或不是目录，请检查配置", watchDir)
	}

	log.Infof("开始监控 %s 申报国家的 XML 文件目录: %s", declareCountry, watchDir)
	service.Watch(watchDir, declareCountry)
}

// RemoverWork 启动文件移动处理程序
func RemoverWork() {
	// 创建队列大小
	queueSize := viper.GetInt("remover.queue_size")
	if queueSize <= 0 {
		queueSize = 200 // 默认队列大小
	}

	// 初始化 NL 国家的文件移动处理程序
	log.Info("正在初始化 NL 国家的文件移动处理程序...")
	config.NlRemover = component.NewRemoveQueue(queueSize, "NL")

	// 启动 NL 国家的文件移动处理程序
	log.Info("启动 NL 国家的文件移动处理程序")
	go config.NlRemover.Run()

	// 初始化 BE 国家的文件移动处理程序
	log.Info("正在初始化 BE 国家的文件移动处理程序...")
	config.BeRemover = component.NewRemoveQueue(queueSize, "BE")

	// 启动 BE 国家的文件移动处理程序
	log.Info("启动 BE 国家的文件移动处理程序")
	go config.BeRemover.Run()

	log.Info("文件移动处理程序已全部启动")
}
